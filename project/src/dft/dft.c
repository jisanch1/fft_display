#define SQRT_32	5.65685424

void dft(double *input_buf, double *real_buf, double *imag_buf, uint8_t n)
{
	uint16_t i, j, k;
	for (i = 0; i < n; i++)
	{
		real_buf[i] = 0;
		imag_buf[i] = 0;
		for (j = 0; j < n; j++)
		{
			k = (i*j) % n;
			real_buf[i] += input_buf[j]*tcos[k]/SQRT_32;
			imag_buf[i] -= input_buf[j]*tsin[k]/SQRT_32;
		}
	}
}



